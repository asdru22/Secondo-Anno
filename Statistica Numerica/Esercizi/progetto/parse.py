# Function to convert data to markdown table
def convert_to_markdown_table(data):
    header = "| I | ME | MR | Acc |\n| ---| --- | --- | --- |\n"
    rows = ""
    c = 1
    for entry in data:
        rows += f"| {c} | ${entry[0]}$ | ${entry[1]}$ | ${entry[2]}$ |\n"
        c+=1
    return header + rows

